void main() {
  int b = 3;
    for (var j = 1; j <= 20; j++) {
      print(j * b);
    }
}

// Kode Dart tersebut akan mencetak deretan bilangan dari 3 hingga 20, dengan kelipatan 3.

// Penjelasan:

// * void main() adalah fungsi utama yang akan dijalankan pertama kali saat program dijalankan.
// * int b = 3; ini adalah deklarasi variabel b dengan tipe data integer dan nilai 3.
// * for (var j = 1; j >= 20; j++)** adalah blok kode yang akan dieksekusi berulang kali selama kondisinya terpenuhi. Kondisinya adalah variabel j dimulai dari 1 dan terus bertambah hingga >= 20.
// * print(j * b); adalah pernyataan yang akan mencetak nilai variabel j dikalikan dengan nilai variabel b.

// Jadi, ketika kode ini dijalankan, akan menghasilkan output sebagai berikut:

// ```
// 3
// 6
// 9
// 12
// 15
// 18
// 21
// 24
// 27
// 30
// 33
// 36
// 39
// 42
// 45
// 48
// 51
// 54
// ```

// Berikut adalah penjelasan dari setiap baris kode:

// * **void main()** adalah fungsi utama yang akan dijalankan pertama kali saat program dijalankan. Fungsi ini tidak memiliki nilai balik, sehingga tipe datanya adalah `void`.
// * **int b = 3;** ini adalah deklarasi variabel b dengan tipe data integer dan nilai 3.
// * **for (var j = 1; j >= 20; j++)** adalah blok kode yang akan dieksekusi berulang kali selama kondisinya terpenuhi. Kondisinya adalah variabel j dimulai dari 1 dan terus bertambah hingga >= 20.
// * **print(j * b);** adalah pernyataan yang akan mencetak nilai variabel j dikalikan dengan nilai variabel b.

// Berikut adalah penjelasan dari setiap baris kode dalam blok `for`:

// * **var j = 1;** ini adalah inisialisasi variabel j dengan nilai 1.
// * **j >= 20;** ini adalah kondisi yang akan diperiksa untuk menentukan apakah blok kode harus diulangi. Jika kondisi ini terpenuhi, maka blok kode akan diulangi. Jika kondisi ini tidak terpenuhi, maka blok kode akan berhenti diulangi.
// * **j++;** ini adalah inkremen variabel j sebesar 1.

