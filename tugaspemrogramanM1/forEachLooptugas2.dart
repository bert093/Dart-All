void main() {
  // Buat list angka
  List<int> angka = [1, 2, 3, 4, 5];

  // Cetak setiap angka dalam list
  for (int i in angka) {
    print(i);
  }
}

// Penjelasan:

// for (int i in angka) adalah sintaks untuk for each loop pada Dart.
// i adalah variabel iterator yang akan mengambil nilai dari setiap elemen dalam list angka.
// print(i) adalah kode untuk mencetak nilai dari variabel i.