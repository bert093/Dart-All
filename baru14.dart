void main() {
  int a = 2;
  int b = 3;
  // Adding a and b
  var c = a + b;
  print("The sum of a and b is $c");
  // Subtracting a and b
  var d = a - b;
  print("The difference between a and b is $d");
  // Using unary minus for d
  var e = -d;
  print("The negation of the difference between a and b is $e");
  // Division of a and b
  var f = b / a;
  print("The result of dividing a and b is $f");
}
