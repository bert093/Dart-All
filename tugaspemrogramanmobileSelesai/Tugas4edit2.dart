void main() {
  // Masukkan nilai-nilai p (prinsipal), t (waktu), dan r (tingkat bunga) di sini
  double p = 1000; // Contoh prinsipal sebesar 1000. Variable p(prinsipal) jumlah uang awal
  double t = 5; // Contoh: waktu sebesar 5 tahun. variable t adalah waktu dalam tahun
  double r = 3.5; // Contoh: tingkat bunga sebesar 3.5%. Variable r adalah tingkat bunga dalam persen

  // Hitung hasil formula
  double hasil = (p * t * r) / 100;

  // Cetak hasil
  print('Hasil dari formula adalah: $hasil');
}
