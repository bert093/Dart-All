void main() {
  int a = 2;
  int b = 3;
  // Menambahkan a dan b
  var c = a + b;
  print("Jumlah a dan b adalah $c");
  // Mengurangkan a dan b
  var d = a - b;
  print("Selisih antara a dan b adalah $d");
  // Menggunakan unary minus untuk d
  var e = -d;
  print("Negasi selisih antara a dan b adalah $e");
  // Pembagian a dan b
  var f = b / a;
  print("Hasil bagi a dan b adalah $f");
  // Variabel dengan nama 'g', 'h', 'i', dan 'j' 
  var g = 5;
  var h = 10;
  var i = 15;
  var j = 20;
  
  print("Variabel g: $g");
  print("Variabel h: $h");
  print("Variabel i: $i");
  print("Variabel j: $j");
}
