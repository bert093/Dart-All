void main() {
  print("Output 1:");
  for(int i = 0; i <= 5; i++) {
    print(i);
  }

  print("Output 2:");
  for(int a = 1; a <= 9; a+=2) {
    print(a);
  }

  print("Output 3:");
  for(int b = 2; b <= 9; b+=2) {
    print(b);
  }

  print("Output 4:");
  for(int c = 5; c >= 1; c--) {
    print(c);
  }

  print("Output 5:");
  for(int d = 5; d >= 1; d-=2) {
    print(d);
  }

  print("Output 6:");
  for(int e = 8; e >= 2; e-=2) {
    print(e);
  }
}
